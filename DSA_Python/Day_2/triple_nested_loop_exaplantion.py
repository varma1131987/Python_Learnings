"""Explanation of Triple Nested Loops
Outer Loop (for i in range(n)):
Runs n times. Represents the first level of iteration.

Middle Loop (for j in range(n)):

Runs n times for each iteration of the outer loop.
Represents the second level of iteration.

Inner Loop (for k in range(n)):

Runs n times for each iteration of the middle loop.
Represents the third level of iteration.
Total Iterations:

The total number of iterations is ( n \times n \times n = n³ )."""


